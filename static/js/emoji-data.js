/**
 * Convert a Slack-style shortcode to its Unicode emoji character.
 * Returns the original :shortcode: format if no match is found.
 *
 * @param {string} shortcode - The emoji shortcode (without colons)
 * @returns {string} The Unicode emoji character, or `:shortcode:` if not found
 */
function emojiToUnicode(shortcode) {
    return EMOJI_DATA[shortcode] || `:${shortcode}:`;
}

/**
 * Search for emojis by name/shortcode substring.
 * Returns up to 50 matching results sorted by relevance (exact prefix match first).
 *
 * @param {string} query - The search query
 * @returns {Array<{name: string, char: string}>} Array of matching emoji objects
 */
function searchEmoji(query) {
    if (!query) return [];
    const q = query.toLowerCase();
    const results = Object.entries(EMOJI_DATA)
        .filter(([name]) => name.includes(q))
        .map(([name, char]) => ({ name, char }));

    // Sort: exact matches first, then prefix matches, then substring matches
    results.sort((a, b) => {
        const aExact = a.name === q;
        const bExact = b.name === q;
        if (aExact && !bExact) return -1;
        if (!aExact && bExact) return 1;

        const aPrefix = a.name.startsWith(q);
        const bPrefix = b.name.startsWith(q);
        if (aPrefix && !bPrefix) return -1;
        if (!aPrefix && bPrefix) return 1;

        return a.name.localeCompare(b.name);
    });

    return results.slice(0, 50);
}

/**
 * Replace all :shortcode: patterns in a text string with their Unicode emoji.
 * Leaves unknown shortcodes as-is in :shortcode: format.
 *
 * @param {string} text - The input text containing :shortcode: patterns
 * @returns {string} The text with shortcodes replaced by Unicode emoji
 */
function replaceEmojiShortcodes(text) {
    if (!text) return text;
    return text.replace(/:([a-zA-Z0-9_+\-]+):/g, (match, shortcode) => {
        const emoji = EMOJI_DATA[shortcode];
        return emoji || match;
    });
}

/**
 * Get all emojis in a given category.
 *
 * @param {string} categoryId - The category ID (e.g., "smileys", "nature")
 * @returns {Array<{name: string, char: string}>} Array of emoji objects in the category
 */
function getEmojisByCategory(categoryId) {
    const category = EMOJI_CATEGORIES.find(c => c.id === categoryId);
    if (!category) return [];
    return category.emojis
        .filter(name => EMOJI_DATA[name])
        .map(name => ({ name, char: EMOJI_DATA[name] }));
}
